# Test 3: Missing column fails
# ---------------------------------------------------------------------------

def test_missing_column_fails(full_input_data, tmp_path):
    """A WBS sheet without the 'Billable' column should fail validation."""
    import sys
    sys.path.insert(0, str(SCRIPTS_DIR))
    from validate_template import validate

    xlsx_path = _generate_valid_workbook(full_input_data, tmp_path)

    wb = openpyxl.load_workbook(str(xlsx_path))
    ws = wb["WBS"]

    # Find and clear the 'Billable' header cell in row 1
    for cell in ws[1]:
        if cell.value and "Billable" in str(cell.value) and "PERT" not in str(cell.value):
            cell.value = "REMOVED"
            break

    modified_path = tmp_path / "no_billable.xlsx"
    wb.save(str(modified_path))

    result = validate(str(modified_path))

    assert result["valid"] is False, f"Expected valid=False, got warnings: {result['warnings']}"


# ---------------------------------------------------------------------------
# Test 4: Formula pattern validated
# ---------------------------------------------------------------------------

def test_formula_pattern_validated(full_input_data, tmp_path):
    """WBS PERT Effort column with a wrong formula pattern should fail."""
    import sys
    sys.path.insert(0, str(SCRIPTS_DIR))
    from validate_template import validate

    xlsx_path = _generate_valid_workbook(full_input_data, tmp_path)

    wb = openpyxl.load_workbook(str(xlsx_path))
    ws = wb["WBS"]

    # Find the PERT Effort column (column H, index 8) and corrupt a leaf formula
    # Leaf rows start at row 3 (row 2 is a phase row, row 3 is WP, row 4+ are leaves)
    # Replace the first formula in the PERT Effort column (col H = 8) with a wrong one
    for row_idx in range(2, ws.max_row + 1):
        cell = ws.cell(row=row_idx, column=8)
        if cell.value and str(cell.value).startswith("=("):
            cell.value = "=A1+B1"  # wrong formula pattern
            break

    modified_path = tmp_path / "bad_formula.xlsx"
    wb.save(str(modified_path))

    result = validate(str(modified_path))

    assert result["valid"] is False, (
        f"Expected valid=False due to bad formula, but got valid=True. "
        f"Warnings: {result['warnings']}"
    )


# ---------------------------------------------------------------------------
# Test 5: column_map returned for valid template
# ---------------------------------------------------------------------------

def test_column_map_returned(full_input_data, tmp_path):
    """validate() on a valid template should return a column_map with sheet keys."""
    import sys
    sys.path.insert(0, str(SCRIPTS_DIR))
    from validate_template import validate

    xlsx_path = _generate_valid_workbook(full_input_data, tmp_path)
    result = validate(str(xlsx_path))

    assert result["valid"] is True
    assert "column_map" in result
    column_map = result["column_map"]

    # Expected sheets (4-sheet refactor)
    for sheet in ("WBS", "Resource Plan", "Risks", "Summary"):
        assert sheet in column_map, f"column_map missing sheet '{sheet}'"
        assert isinstance(column_map[sheet], dict), (
            f"column_map['{sheet}'] should be a dict of header->column_letter"
        )

    # Check a known WBS column is present
    wbs_map = column_map["WBS"]
    # "ID" should map to column "A"
    assert "ID" in wbs_map, f"WBS column_map missing 'ID': {wbs_map}"
    assert wbs_map["ID"] == "A", f"Expected WBS ID -> A, got {wbs_map['ID']}"


# ---------------------------------------------------------------------------
# Test 6: Warnings for extra columns
# ---------------------------------------------------------------------------

def test_warnings_for_extra_columns(full_input_data, tmp_path):
    """A template with an extra column in WBS should be valid with a warning."""
    import sys
    sys.path.insert(0, str(SCRIPTS_DIR))
    from validate_template import validate

    xlsx_path = _generate_valid_workbook(full_input_data, tmp_path)

    wb = openpyxl.load_workbook(str(xlsx_path))
    ws = wb["WBS"]

    # Add an extra column after the last header
    max_col = ws.max_column
    ws.cell(row=1, column=max_col + 1, value="CustomExtraColumn")

    modified_path = tmp_path / "extra_col.xlsx"
    wb.save(str(modified_path))

    result = validate(str(modified_path))

    assert result["valid"] is True, f"Expected valid=True, errors: {result['errors']}"
    assert len(result["warnings"]) > 0, "Expected at least one warning for extra column"
    assert any("CustomExtraColumn" in w or "extra" in w.lower() for w in result["warnings"]), (
        f"Expected a warning about extra column, got: {result['warnings']}"
    )


# ---------------------------------------------------------------------------
# Test 7: CLI interface
# ---------------------------------------------------------------------------

def test_cli_interface(full_input_data, tmp_path):
    """CLI: python3 validate_template.py --template <path> exits 0 for valid template."""
    xlsx_path = _generate_valid_workbook(full_input_data, tmp_path)

    cli_script = SCRIPTS_DIR / "validate_template.py"
    proc = subprocess.run(
        [sys.executable, str(cli_script), "--template", str(xlsx_path)],
        capture_output=True,
        text=True,
        cwd=str(SCRIPTS_DIR),
    )

    assert proc.returncode == 0, (
        f"CLI exited with {proc.returncode}.\n"
        f"stdout: {proc.stdout}\nstderr: {proc.stderr}"
    )

    # Output should be valid JSON
    output = json.loads(proc.stdout)
    assert output["valid"] is True


def test_cli_exits_1_for_invalid(full_input_data, tmp_path):
    """CLI: exits 1 for invalid template (missing sheet)."""
    xlsx_path = _generate_valid_workbook(full_input_data, tmp_path)

    wb = openpyxl.load_workbook(str(xlsx_path))
    del wb["Risks"]
    bad_path = tmp_path / "invalid.xlsx"
    wb.save(str(bad_path))

    cli_script = SCRIPTS_DIR / "validate_template.py"
    proc = subprocess.run(
        [sys.executable, str(cli_script), "--template", str(bad_path)],
        capture_output=True,
        text=True,
        cwd=str(SCRIPTS_DIR),
    )

    assert proc.returncode == 1, (
        f"Expected exit code 1 for invalid template, got {proc.returncode}.\n"
        f"stdout: {proc.stdout}"
    )
