"""Tests for validate_template.py — TDD: tests written before implementation."""
import json
import subprocess
import sys
from pathlib import Path

import openpyxl
import pytest

# The scripts directory is the package root; tests run from there.
SCRIPTS_DIR = Path(__file__).parent.parent


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _generate_valid_workbook(full_input_data, tmp_path):
    """Generate a valid workbook using the generate_excel pipeline.

    Returns the Path to the generated .xlsx file.
    """
    import sys
    sys.path.insert(0, str(SCRIPTS_DIR))
    from generate_excel import main as generate_main

    json_path = tmp_path / "input.json"
    json_path.write_text(json.dumps(full_input_data))
    xlsx_path = tmp_path / "template.xlsx"
    rc = generate_main(str(json_path), str(xlsx_path))
    assert rc == 0, "generate_excel.main() failed — check fixtures"
    return xlsx_path


# ---------------------------------------------------------------------------
# Test 1: Valid template passes
# ---------------------------------------------------------------------------

def test_valid_template_passes(full_input_data, tmp_path):
    """A workbook generated by generate_excel should validate as True."""
    import sys
    sys.path.insert(0, str(SCRIPTS_DIR))
    from validate_template import validate

    xlsx_path = _generate_valid_workbook(full_input_data, tmp_path)
    result = validate(str(xlsx_path))

    assert result["valid"] is True, f"Expected valid=True, got errors: {result['errors']}"
    assert result["errors"] == []


